"""
This is a multi-line comment.
It can span multiple lines.
Python ignores this when running the code.
"""
print("Hello, World!")

'''
This is another way
to write a multi-line comment.
'''
print("Python is awesome!")


# Example 2: Using - as a separator
print("2025", "02", "04", sep="-")

#  Using sep="" to Remove Spaces
print("H", "E", "L", "L", "O", sep="")

#  Using sep="\n" to Print Each Item on a New Line
print("Apple", "Banana", "Cherry", sep="\n")

# You can use both sep and end together:
print("A", "B", "C", sep="-", end="!!\n")

